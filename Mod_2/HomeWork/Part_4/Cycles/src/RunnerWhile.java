public class RunnerWhile {

    public static void main(String[] args) {

        System.out.println("Part 1");
        int count = 200000;
        while(count <= 210000) {
            System.out.println("Ticket №" + count++);
        }

        System.out.println("Part 1");
        count = 220000;
        while(count <= 235000) {
            System.out.println("Ticket №" + count++);
        }
    }
}
